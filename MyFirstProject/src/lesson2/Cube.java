package lesson2;

public class Cube {
	int length;
	int bredth;
	int height;
	
	// constructor
	Cube(){
		//System.out.println("We are in constructer");
		length = 10;
		bredth = 20;
		height = 30;
	}
	// constructor overloading
	Cube(int l, int b, int h){
		//System.out.println("We are in constructer");
		length = l;
		bredth = b;
		height = h;
	}
	public int getCubeVolume() {
		return (length*bredth*height);
	}
} 	 	
